package tutorial.java.string;

public class StringClassExample {

    public static void main (String[] args) {

        // String literal
        String s1="String1";
        System.out.println(s1);
        s1 = "abv";
        System.out.println(s1);

        // Using new Keyword
        String s2= new String("String2");
        System.out.println(s2);
        s2 = "abvs";
        System.out.println(s2);
    }

}
